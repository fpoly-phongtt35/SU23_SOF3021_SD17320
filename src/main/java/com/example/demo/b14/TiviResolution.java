package com.example.demo.b14;

public class TiviResolution {

    private Integer doPhanGiai;
    private Long soLuong;

    public TiviResolution() {

    }

    public TiviResolution(Integer doPhanGiai, Long soLuong) {
        this.doPhanGiai = doPhanGiai;
        this.soLuong = soLuong;
    }

    public Integer getDoPhanGiai() {
        return doPhanGiai;
    }

    public void setDoPhanGiai(Integer doPhanGiai) {
        this.doPhanGiai = doPhanGiai;
    }

    public Long getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(Long soLuong) {
        this.soLuong = soLuong;
    }
}
